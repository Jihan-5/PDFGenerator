def export_to_json(sections, filename):
    """Export sections data to JSON for external use, excluding url and purpose."""
    try:
        json_data = [
            {k: v for k, v in section.items() if k not in ['url', 'purpose']}  # Exclude url and purpose
            for section in sections
        ]
        with open(filename, 'w') as f:
            json.dump(json_data, f, indent=2)
        print(f"JSON exported: {filename}")
    except Exception as e:
        print(f"Error exporting JSON: {e}")


def generate_docx(output_filename, sections):
    doc = Document()

    # Set default styles with reduced spacing
    styles = doc.styles
    default_style = styles['Normal']
    default_style.font.name = 'Calibri'
    default_style.font.size = Pt(10)  # Smaller font size for compactness
    default_style.paragraph_format.space_after = Pt(6)  # Reduced spacing after paragraphs
    default_style.paragraph_format.space_before = Pt(0)

    # Heading styles with reduced spacing
    heading1_style = styles['Heading 1']
    heading1_style.font.size = Pt(14)
    heading1_style.paragraph_format.space_after = Pt(8)

    heading2_style = styles['Heading 2']
    heading2_style.font.size = Pt(12)
    heading2_style.paragraph_format.space_after = Pt(6)

    # Add document title
    doc.add_heading('Accord Fintech API Summary', 0).paragraph_format.space_after = Pt(12)

    subsection_map = {
        'company_master': '1',
        'industry_master': '2',
        'house_master': '3',
        'stock_exchange_master': '4',
        'company_listings': '5',
        'company_address': '6',
        'company_profile': '7',
        'company_registrar_master': '8',
        'company_registrar_data': '9',
        'company_board_director': '10',
        'company_finance_balancesheet': '11',
        'company_finance_balancesheet_cons': '12',
        'company_finance_profitloss': '13',
        'company_finance_profitloss_cons': '14',
        'company_finance_cashflow': '15',
        'company_finance_cashflow_cons': '16',
        'company_finance_ratio': '17',
        'company_finance_ratio_cons': '18',
        'company_results': '19',
        'company_results_cons': '20',
        'company_equity': '21',
        'company_equity_cons': '22',
        'company_shareholders_details': '23',
        'shareholding_category_master': '24',
        'company_shareholding_pattern': '25',
        'monthly_price_bse': '26',
        'monthly_price_nse': '27',
    }

    for section in sections:
        # Normalize the name: lowercase, strip spaces, replace spaces with underscores
        normalized_name = section['name'].strip().lower().replace(' ', '_')
        section_number = subsection_map.get(normalized_name, '')

        # Section Title
        title = f"{section_number}. {section['name']}" if section_number else section['name']
        doc.add_heading(title, level=1)
        # Overview
        doc.add_heading("Overview", level=2)
        p = doc.add_paragraph(section['description'])
        p.paragraph_format.space_after = Pt(6)

        # Purpose
        doc.add_heading("Purpose", level=2)
        for bullet in section.get('purpose', []):
            p = doc.add_paragraph(bullet, style='List Bullet')
            p.paragraph_format.space_after = Pt(4)

        # Add one-line spacing after Purpose section
        p = doc.add_paragraph()
        p.paragraph_format.space_after = Pt(10)  # One line spacing (matches Normal font size)

        # Field Definitions
        doc.add_heading("Field Definitions", level=2)

        # Add one-line spacing before table
        p = doc.add_paragraph()
        p.paragraph_format.space_after = Pt(10)  # One line spacing

        # Field Definitions Table
        table = doc.add_table(rows=1, cols=3)
        table.autofit = True
        table.allow_autofit = True
        table.style = 'Table Grid'

        # Set smaller cell margins for compactness
        for row in table.rows:
            for cell in row.cells:
                cell.paragraphs[0].paragraph_format.space_after = Pt(2)
                cell.paragraphs[0].paragraph_format.space_before = Pt(2)
                for paragraph in cell.paragraphs:
                    for run in paragraph.runs:
                        run.font.size = Pt(9)  # Smaller font size for table content

        # Set column headers
        hdr_cells = table.rows[0].cells
        hdr_cells[0].text = 'Field Name'
        hdr_cells[1].text = 'Field Type'
        hdr_cells[2].text = 'What It Means'

        # Populate table with field data
        for field in section.get('key_fields', []):
            row_cells = table.add_row().cells
            row_cells[0].text = field['field_name']
            row_cells[1].text = field['field_type']
            row_cells[2].text = field['description']

        # Add one-line spacing after table
        p = doc.add_paragraph()
        p.paragraph_format.space_after = Pt(10)  # One line spacing

    doc.save(output_filename)
    print(f"Word doc generated: {output_filename}")


